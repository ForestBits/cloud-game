package arcadegame1;

import org.jsfml.graphics.Color;


public class ImageParticle 
{
    public final int x;
    public final int y;
    public final Color color;
    
    public ImageParticle(int x, int y, Color color)
    {
        this.x = x;
        this.y = y;
        this.color = color;
    }
}
